# Mostrar na tela todos os números inteiros pares que estão entre 1 e 50

# Fazendo Salto == 2, o processador vai ler metade da lista, ou seja, vai processar mais rápido.
for c in range(2, 51, 2):
    # print(end='.') - feito para checar quantos laços estava fazendo antes de imprimir um número.
    print(c, end=' ')


